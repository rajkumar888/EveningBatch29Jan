package com.day1;

public class PrimitiveDataTyesDemo {

	static int intx;
	static int bytex;
	static int shortx;
	static int longx;
	
	static float floata;
	static double doublea;
	
	static char charA;
	
	static boolean booleanA;
	
	public static void main(String[] args) {
		
		
		System.out.println(intx);
		System.out.println(bytex);
		System.out.println(shortx);
		System.out.println(longx);
		System.out.println(".........................");
		System.out.println(floata);
		System.out.println(doublea);
		System.out.println(".........................");
		System.out.println(charA);
		System.out.println(".........................");
		System.out.println(booleanA);
		
		
	}

}
